const { Schema, model } = require('mongoose');

const customerSchema = new Schema({
	firstName: {
		type: String,
		required: true,
	},
	lastName: {
		type: String,
		required: true,
	},
	email: {
		type: String,
		required: true,
	},
	phoneNumber: {
		type: Number,
		required: true,
	},
	city: {
		type: String,
		required: true,
	},
	address: {
		type: String,
		required: true,
	},
	zipCode: {
		type: Number,
		required: true,
	},
});

module.exports = model('Customer', customerSchema);
